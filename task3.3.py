a = 10
b = 3

# Сложение
addition = a + b
# Вычитание
subtraction = a - b
# Деление
division = a / b
# Умноженире
multiplication = a * b
# Степень
exponent = a ** b
# Остаток от деления
modulus = a % b
# Деление с округлением
floor_division = a // b

# Печатаю резeультат
print(f"Результат сложения: {a} + {b} = {addition}")
print(f"Результат вычитания: {a} - {b} = {subtraction}")
print(f"Результат деления: {a} / {b} = {division}")
print(f"Результат умножения: {a} * {b} = {multiplication}")
print(f"Результат степени: {a} ** {b} = {exponent}")
print(f"Результат остатока от деления (модуль): {a} % {b} = {modulus}")
print(f"Результат деления с округлением: {a} // {b} = {floor_division}")
